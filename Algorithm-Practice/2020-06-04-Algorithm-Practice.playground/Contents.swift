/*
 \    /\
  )  ( ')
 (  /  )
  \(__)|
 */

print("\\    /\\\n )  ( ')\n(  /  )\n \\(__)|")

/*
 배운점 1
 - 백슬래쉬 (\) 를 print 할 경우에는 print 할 백슬래쉬 앞에 백슬래쉬를 하나 더 붙인다
    - 예를 들어 "\" 이렇게 print 하고싶은 경우에는 print("\\")를 해야한다.
 
 배운점 2
 - 한 줄로 된 긴 문장을 다른 줄로 바꾸어 print 하고 싶은 경우 "\n" 을 사용한다.
    - 예를 들어 "hello world" 를 1번 줄에 "hello" 2번 줄에 "world"를 print 하고 싶으면 print("hello\nworld")
 */

